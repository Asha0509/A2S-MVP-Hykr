// Sum the base price of a room's full spec — used to seed cost rollups so
// the summary is never ₹0 even when the live catalog endpoint is empty.
export const roomBaseTotal = (roomKey) => {
    const r = ROOM_BREAKDOWN[roomKey];
    if (!r) return 0;
    return r.items.reduce((s, it) => s + it.price, 0);
};
